package com.cen4802.module7;

public class Calculator
{
	public Calculator()
	{
		
	}
	
	public int addition(int x, int y)
	{
		return x + y;
	}
	
	public int subtraction(int a, int b)
	{
		return a - b;
	}
	
	public int multiply(int c, int d)
	{
		return c * d;
	}
	
	public double divide(double f, double g)
	{
		return f / g;
	}
}
